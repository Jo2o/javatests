    static void minimumBribes(int[] q) {
        List<Integer> resultQueue = Arrays.stream(q).boxed().collect(toList());
        List<Integer> orderedQueue = IntStream.rangeClosed(1, resultQueue.size()).boxed().collect(toList());
        int queueSize = resultQueue.size();
        int bribesCount = 0;

        for (int i = 1; i <= queueSize; i++) {
            int maxBribesPerPerson = 2;
            int number = resultQueue.get(i - 1);
            int orderedQueueIdx = orderedQueue.indexOf(number);
            int resultQueueIdx = resultQueue.indexOf(number);

            while ((resultQueueIdx != orderedQueueIdx) && (maxBribesPerPerson != 0)) {
                if (orderedQueueIdx > resultQueueIdx) {
                    Collections.swap(orderedQueue, orderedQueueIdx, orderedQueueIdx - 1);
                    bribesCount++;
                    orderedQueueIdx -= 1;
                    maxBribesPerPerson--;
                } else {
                    break;
                }
            }
            if (orderedQueue.equals(resultQueue)) {
                System.out.println(bribesCount);
                return;
            }
        }
        System.out.println("Too chaotic");
    }

